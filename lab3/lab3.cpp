/*
 * Лабораторная работа 3 — Параллельное сложение матриц (MPI_Scatter / MPI_Allgather)
 *
 * Задача: процесс 0 генерирует две матрицы A и B, раздаёт каждому процессу
 * равное количество строк из обеих матриц, каждый процесс поэлементно складывает
 * свои строки (C = A + B), затем результат собирается на ВСЕХ процессах через
 * MPI_Allgather.
 *
 * Ключевое отличие от лабы 2:
 *   - Лаба 2 использует MPI_Reduce: результат приходит только к одному root-процессу.
 *   - Здесь используется MPI_Allgather: каждый процесс получает ПОЛНЫЙ результат C.
 *     Это нужно, когда после сборки каждый процесс будет использовать C в
 *     дальнейших вычислениях (например, умножение — следующий шаг алгоритма).
 *
 * Размеры (при запуске с 4 процессами, m=4):
 *   n    = m * size = 16  — количество строк в матрице
 *   m    = 4             — количество столбцов (и строк — матрица квадратная n×m нет, n×m = 16×4)
 *   Матрицы хранятся в памяти как плоский массив строка за строкой (row-major):
 *     элемент [row][col] → arr[row * m + col]
 *
 * Схема взаимодействия:
 *   процесс 0 генерирует A_global[n×m], B_global[n×m]
 *       │
 *   MPI_Scatter A → A_local[rows_per_proc × m]  у каждого процесса
 *   MPI_Scatter B → B_local[rows_per_proc × m]  у каждого процесса
 *       │
 *   каждый: C_local[i] = A_local[i] + B_local[i]  (параллельно)
 *       │
 *   MPI_Allgather: каждый вносит C_local → ВСЕ получают C_global[n×m]
 *       │
 *   процесс 0 печатает A, B, C в матричном формате
 */

#include <mpi.h>
#include <iostream>
#include <stdlib.h>   // rand(), srand()
#include <time.h>     // time()
using namespace std;

// ---------------------------------------------------------------------------
// Вспомогательная функция: заполнение плоского массива случайными числами
// ---------------------------------------------------------------------------
// Принимает указатель на массив и размеры матрицы n×m.
// Заполняет n*m элементов значениями из диапазона [-9..9].
// Матрица передаётся как одномерный массив: элемент [i][j] = arr[i*m + j].
void fillmatrix(int *arr, int n, int m)
{
    for (int i = 0; i < m * n; i++)
    {
        arr[i] = rand() % 19 - 9;
    }
}

int main(int argc, char **argv)
{
    // -------------------------------------------------------------------------
    // Инициализация MPI-окружения
    // -------------------------------------------------------------------------
    MPI_Init(&argc, &argv);

    int rank, size;
    MPI_Comm_rank(MPI_COMM_WORLD, &rank); // Номер текущего процесса [0..size-1]
    MPI_Comm_size(MPI_COMM_WORLD, &size); // Всего процессов

    // -------------------------------------------------------------------------
    // Вычисление размеров
    // -------------------------------------------------------------------------
    const int m            = 4;              // Столбцов в матрице (= строк, матрица n×m)
    const int n            = m * size;       // Строк в глобальной матрице
    const int rows_per_proc = n / size;      // Строк на процесс = m (при n = m*size всегда равно m)
    const int local_size   = rows_per_proc * m; // Элементов в локальном куске одной матрицы
    const int global_size  = n * m;          // Элементов в полной матрице

    // -------------------------------------------------------------------------
    // Выделение памяти
    // -------------------------------------------------------------------------
    // A_global и B_global нужны ТОЛЬКО процессу 0 (он генерирует данные).
    // Остальные процессы не выделяют под них память — при вызове Scatter
    // они могут передавать nullptr в качестве sendbuf.
    int *A_global = nullptr;
    int *B_global = nullptr;

    // Локальные куски и результирующий массив — у КАЖДОГО процесса.
    // C_global получают все процессы через Allgather, поэтому выделяется везде.
    int *A_local  = new int[local_size];
    int *B_local  = new int[local_size];
    int *C_local  = new int[local_size];
    int *C_global = new int[global_size];

    // -------------------------------------------------------------------------
    // Генерация матриц (только на процессе 0)
    // -------------------------------------------------------------------------
    if (rank == 0)
    {
        srand(time(NULL));
        A_global = new int[global_size];
        B_global = new int[global_size];
        fillmatrix(A_global, n, m); // Заполняем A случайными числами
        fillmatrix(B_global, n, m); // Заполняем B случайными числами
    }

    // -------------------------------------------------------------------------
    // MPI_Scatter — раздача строк матриц всем процессам
    // -------------------------------------------------------------------------
    // Scatter нарезает глобальный массив на size равных кусков и отправляет
    // i-й кусок процессу с rank == i. Здесь выполняется дважды — для A и для B.
    //
    // Параметры MPI_Scatter:
    //   A_global    — исходный массив (значим только на root = 0)
    //   local_size  — сколько элементов отправить каждому процессу
    //   MPI_INT     — тип отправляемых данных
    //   A_local     — буфер для приёма локального куска (у текущего процесса)
    //   local_size  — сколько элементов принять
    //   MPI_INT     — тип принимаемых данных
    //   0           — rank корневого процесса (кто раздаёт)
    //   MPI_COMM_WORLD
    //
    // После первого Scatter:
    //   процесс 0 → A_global[0 .. local_size-1]           (строки 0..rows_per_proc-1)
    //   процесс 1 → A_global[local_size .. 2*local_size-1] (следующие rows_per_proc строк)
    //   и т.д.
    MPI_Scatter(A_global, local_size, MPI_INT, A_local, local_size, MPI_INT, 0, MPI_COMM_WORLD);
    MPI_Scatter(B_global, local_size, MPI_INT, B_local, local_size, MPI_INT, 0, MPI_COMM_WORLD);

    // -------------------------------------------------------------------------
    // Параллельное поэлементное сложение (ядро вычислений)
    // -------------------------------------------------------------------------
    // Каждый процесс обрабатывает только свой кусок — rows_per_proc строк.
    // Все процессы выполняют этот цикл одновременно: именно здесь и происходит
    // параллельное ускорение. При size=4 каждый считает в 4 раза меньше, чем
    // последовательный алгоритм на одном процессе.
    for (int i = 0; i < local_size; i++)
    {
        C_local[i] = A_local[i] + B_local[i];
    }

    // -------------------------------------------------------------------------
    // MPI_Allgather — сборка результата на ВСЕХ процессах
    // -------------------------------------------------------------------------
    // Отличие от MPI_Gather:
    //   MPI_Gather    → результат C_global появляется только на root-процессе.
    //   MPI_Allgather → C_global получают ВСЕ процессы (эквивалент Gather + Bcast,
    //                   но реализован эффективнее через алгоритм "butterfly").
    //
    // Когда использовать Allgather: когда после сборки каждый процесс должен
    // продолжить вычисления над полным результатом (например, умножение матриц:
    // после сбора C каждый процесс будет умножать его на следующую матрицу).
    //
    // Параметры MPI_Allgather:
    //   C_local     — буфер с локальным вкладом текущего процесса (send)
    //   local_size  — сколько элементов отправляет каждый процесс
    //   MPI_INT     — тип отправляемых данных
    //   C_global    — буфер для полного результата (recv, выделен у ВСЕХ)
    //   local_size  — сколько элементов принять от каждого одного процесса
    //   MPI_INT     — тип принимаемых данных
    //   MPI_COMM_WORLD
    //
    // Нет параметра root — он не нужен, потому что результат идёт всем.
    //
    // После Allgather у каждого процесса в C_global:
    //   [0..local_size-1]             — кусок от процесса 0
    //   [local_size..2*local_size-1]  — кусок от процесса 1
    //   и т.д. в порядке возрастания rank
    MPI_Allgather(C_local, local_size, MPI_INT, C_global, local_size, MPI_INT, MPI_COMM_WORLD);

    // -------------------------------------------------------------------------
    // Вывод матриц (только процесс 0 — у него есть A_global и B_global)
    // -------------------------------------------------------------------------
    // C_global есть у всех, но печатать одно и то же 4 раза не нужно.
    if (rank == 0)
    {
        // Вывод матрицы A в табличном формате:
        // печатаем элемент за элементом, после каждых m элементов — перевод строки
        cout << "A:" << endl;
        for (int i = 0; i < global_size; i++)
        {
            cout << A_global[i] << " ";
            if ((i + 1) % m == 0)
                cout << endl;
        }

        cout << "B:" << endl;
        for (int i = 0; i < global_size; i++)
        {
            cout << B_global[i] << " ";
            if ((i + 1) % m == 0)
                cout << endl;
        }

        // C = A + B — вычислено параллельно, собрано через Allgather
        cout << "C = A + B:" << endl;
        for (int i = 0; i < global_size; i++)
        {
            cout << C_global[i] << " ";
            if ((i + 1) % m == 0)
                cout << endl;
        }

        // Освобождаем глобальные массивы, выделенные только на процессе 0
        delete[] A_global;
        delete[] B_global;
    }

    // -------------------------------------------------------------------------
    // Освобождение памяти, выделенной на каждом процессе
    // -------------------------------------------------------------------------
    delete[] A_local;
    delete[] B_local;
    delete[] C_local;
    delete[] C_global; // C_global выделен у всех процессов — освобождаем везде

    // -------------------------------------------------------------------------
    // Завершение MPI-окружения
    // -------------------------------------------------------------------------
    MPI_Finalize();
    return 0;
}
