/*
 * Лабораторная работа 1 — Точечные коммуникации MPI (MPI_Send / MPI_Recv)
 *
 * Задача: каждый процесс генерирует локальный массив случайных чисел,
 * затем все процессы, кроме последнего, отправляют свои данные последнему.
 * Последний процесс собирает глобальный массив и выводит его.
 *
 * Схема взаимодействия:
 *   процесс 0  ──Send──┐
 *   процесс 1  ──Send──┤──► процесс (size-1) собирает и печатает
 *   процесс 2  ──Send──┘
 *   процесс (size-1) копирует свои данные напрямую (без Send самому себе)
 */

#include <mpi.h>      // Заголовок MPI: все типы, функции и константы стандарта
#include <iostream>
#include <cstdlib>    // rand(), srand()
#include <ctime>      // time()
using namespace std;

// Количество элементов в локальном массиве каждого процесса.
// Глобальный массив будет размером m * size.
const int m = 4;

int main(int argc, char **argv)
{
    // -------------------------------------------------------------------------
    // Инициализация MPI-окружения
    // -------------------------------------------------------------------------
    // MPI_Init ДОЛЖНА быть первым вызовом MPI в программе.
    // Она запускает механизмы связи между процессами, выделяет внутренние
    // буферы и синхронизирует старт всех процессов.
    // Без MPI_Init любой последующий вызов MPI — неопределённое поведение.
    // argc/argv передаются, потому что некоторые реализации MPI используют
    // аргументы командной строки для настройки (в MS-MPI не нужны, но
    // передавать их — хорошая практика для переносимости).
    MPI_Init(&argc, &argv);

    // -------------------------------------------------------------------------
    // Получение идентификатора и общего числа процессов
    // -------------------------------------------------------------------------
    int rank, size;

    // rank — уникальный номер текущего процесса в коммуникаторе (0 .. size-1).
    // Каждый процесс вызывает эту функцию и получает СВОЙ номер.
    // Коммуникатор MPI_COMM_WORLD объединяет все запущенные процессы.
    MPI_Comm_rank(MPI_COMM_WORLD, &rank);

    // size — сколько всего процессов запущено (задаётся флагом -n у mpiexec).
    // В нашем случае mpiexec -n 4 запускает 4 процесса: rank = 0, 1, 2, 3.
    MPI_Comm_size(MPI_COMM_WORLD, &size);

    // -------------------------------------------------------------------------
    // Генерация локального массива случайных чисел
    // -------------------------------------------------------------------------
    // Добавляем rank к зерну, чтобы у каждого процесса была своя
    // последовательность случайных чисел. Без этого все процессы,
    // запущенные в одну секунду, сгенерируют одинаковые массивы.
    srand(time(NULL) + rank);

    int x_local[m];
    for (int i = 0; i < m; i++)
    {
        // rand() % 19 даёт [0..18], вычитаем 9 → диапазон [-9..9]
        x_local[i] = rand() % 19 - 9;
    }

    // -------------------------------------------------------------------------
    // Каждый процесс печатает свой локальный массив
    // -------------------------------------------------------------------------
    // Все процессы выполняют этот блок одновременно (параллельно).
    // Порядок строк в выводе не гарантирован — зависит от планировщика ОС.
    cout << "process " << rank << ": ";
    for (int j = 0; j < m; j++)
    {
        cout << x_local[j] << " ";
    }
    cout << endl;

    // -------------------------------------------------------------------------
    // Точечные коммуникации: сбор данных на последнем процессе
    // -------------------------------------------------------------------------
    // Ветвление по rank — это фундаментальный приём в MPI: один и тот же
    // исходный код запускается на всех процессах, но if/else по rank
    // заставляет каждый процесс выполнять свою роль.
    //
    // Роль "последнего" процесса (rank == size-1): сборщик данных.
    // Роль остальных процессов: отправители.
    if (rank == size - 1)
    {
        // Последний процесс выделяет глобальный массив под все данные
        int n = m * size;
        int x_global[n];

        // Получаем данные от каждого из остальных процессов по очереди.
        // MPI_Recv — блокирующий приём: выполнение не продолжится,
        // пока сообщение не будет получено полностью.
        //
        // Параметры MPI_Recv:
        //   &x_global[i * m]  — адрес в буфере, куда записать данные
        //                        (каждый процесс пишет в свой сегмент)
        //   m                 — сколько элементов ожидаем принять
        //   MPI_INT           — тип элементов (должен совпадать с Send)
        //   i                 — rank отправителя (получаем строго от процесса i)
        //   0                 — тег сообщения (произвольная метка, должна
        //                        совпадать с тегом в MPI_Send)
        //   MPI_COMM_WORLD    — коммуникатор
        //   MPI_STATUS_IGNORE — статус сообщения нам не нужен (источник,
        //                        тег, ошибки), поэтому игнорируем
        for (int i = 0; i < size - 1; i++)
        {
            MPI_Recv(&x_global[i * m], m, MPI_INT, i, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);
        }

        // Последний процесс не отправляет данные сам себе — копирует напрямую.
        // (MPI_Send самому себе возможен, но избыточен и может вызвать deadlock
        // при определённых условиях буферизации.)
        for (int j = 0; j < m; j++)
        {
            x_global[(size - 1) * m + j] = x_local[j];
        }

        // Вывод собранного глобального массива
        for (int i = 0; i < n; i++)
        {
            cout << x_global[i] << " ";
        }
        cout << endl;
    }
    else
    {
        // Все процессы, кроме последнего, отправляют свой массив.
        // MPI_Send — блокирующая отправка: возврат из функции означает,
        // что буфер можно переиспользовать (данные скопированы в MPI-буфер).
        //
        // Параметры MPI_Send:
        //   x_local        — указатель на буфер с данными
        //   m              — количество элементов
        //   MPI_INT        — тип элементов
        //   size - 1       — rank получателя (последний процесс)
        //   0              — тег сообщения (должен совпадать с тегом в Recv)
        //   MPI_COMM_WORLD — коммуникатор
        MPI_Send(x_local, m, MPI_INT, size - 1, 0, MPI_COMM_WORLD);
    }

    // -------------------------------------------------------------------------
    // Завершение MPI-окружения
    // -------------------------------------------------------------------------
    // MPI_Finalize ДОЛЖНА быть последним вызовом MPI в программе.
    // Освобождает все внутренние ресурсы MPI (буферы, соединения).
    // После Finalize никакие MPI-функции вызывать нельзя.
    // Все процессы должны дойти до Finalize — она неявно синхронизирует их.
    MPI_Finalize();
    return 0;
}
