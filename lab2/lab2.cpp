/*
 * Лабораторная работа 2 — Коллективные операции MPI (MPI_Scatter / MPI_Reduce)
 *
 * Задача: процесс 0 генерирует глобальный массив, раздаёт каждому процессу
 * равную долю (Scatter), каждый процесс находит максимум в своей доле,
 * затем коллективно вычисляется глобальный максимум (Reduce).
 *
 * Отличие от точечных коммуникаций (лаба 1):
 *   - MPI_Send/Recv работают между ДВУМЯ процессами (один отправитель,
 *     один получатель).
 *   - Коллективные операции (Scatter, Reduce, Bcast, Gather…) охватывают
 *     ВЕСЬ коммуникатор одновременно. Все процессы обязаны вызвать их —
 *     пропуск вызова любым из них приведёт к зависанию программы.
 *
 * Схема:
 *   процесс 0 генерирует x_global[n]
 *       │
 *   MPI_Scatter: x_global нарезается на size кусков по m элементов,
 *       каждый кусок отправляется соответствующему процессу
 *       │
 *   каждый процесс: local_max = max(x_local)
 *       │
 *   MPI_Reduce: все local_max сводятся к global_max операцией MAX на процессе 1
 */

#include <mpi.h>
#include <iostream>
#include <stdlib.h>
#include <time.h>
using namespace std;

int main(int argc, char **argv)
{
    // -------------------------------------------------------------------------
    // Инициализация MPI-окружения
    // -------------------------------------------------------------------------
    // Запускает механизм связи между процессами. Обязательный первый вызов.
    // До MPI_Init переменные rank и size неизвестны.
    MPI_Init(&argc, &argv);

    // -------------------------------------------------------------------------
    // Получение идентификатора и числа процессов
    // -------------------------------------------------------------------------
    int rank, size;

    // rank — номер текущего процесса (0 .. size-1).
    // Один и тот же исходный код запускается на всех процессах,
    // но каждый знает свой rank и ветвится по нему.
    MPI_Comm_rank(MPI_COMM_WORLD, &rank);

    // size — общее количество процессов (задаётся через mpiexec -n <N>).
    MPI_Comm_size(MPI_COMM_WORLD, &size);

    // -------------------------------------------------------------------------
    // Объявление переменных
    // -------------------------------------------------------------------------
    const int m = 10;       // Размер локального куска у каждого процесса
    const int n = m * size; // Размер глобального массива (сумма всех кусков)

    int x_local[m];         // Локальный кусок, который получит текущий процесс
    int global_max;         // Итоговый максимум по всему массиву (результат Reduce)
    int *x_global = nullptr;// Глобальный массив — нужен ТОЛЬКО на процессе 0

    // -------------------------------------------------------------------------
    // Инициализация глобального массива (только на процессе 0)
    // -------------------------------------------------------------------------
    // Коллективные операции требуют, чтобы данные существовали только на
    // корневом процессе (root). Остальным процессам достаточно передать nullptr —
    // они всё равно получат свой кусок через Scatter.
    if (rank == 0)
    {
        x_global = new int[n]; // Выделяем память только там, где она нужна
        srand(time(NULL));
        for (int i = 0; i < n; i++)
        {
            x_global[i] = rand() % 10; // Случайные числа в диапазоне [0..9]
        }
    }

    // -------------------------------------------------------------------------
    // MPI_Scatter — раздача кусков глобального массива всем процессам
    // -------------------------------------------------------------------------
    // Scatter делает то, что пришлось бы делать вручную через цикл MPI_Send:
    // нарезает массив x_global на size равных кусков и отправляет i-й кусок
    // процессу с rank == i. Намного эффективнее цикла Send/Recv.
    //
    // Параметры MPI_Scatter:
    //   x_global       — исходный массив (только root использует его реально;
    //                    остальные могут передать nullptr)
    //   m              — сколько элементов отправить КАЖДОМУ процессу
    //   MPI_INT        — тип элементов в исходном массиве
    //   x_local        — буфер, куда текущий процесс получит свой кусок
    //   m              — сколько элементов принять (обычно = send count)
    //   MPI_INT        — тип элементов в принимающем буфере
    //   0              — rank корневого процесса (кто "раздаёт")
    //   MPI_COMM_WORLD — коммуникатор
    //
    // После возврата из Scatter каждый процесс имеет свои m элементов в x_local:
    //   процесс 0 → x_global[0..m-1]
    //   процесс 1 → x_global[m..2m-1]
    //   и т.д.
    MPI_Scatter(x_global, m, MPI_INT,
                x_local,  m, MPI_INT,
                0, MPI_COMM_WORLD);

    // -------------------------------------------------------------------------
    // Каждый процесс выводит полученный локальный кусок
    // -------------------------------------------------------------------------
    cout << "process " << rank << " x_local: ";
    for (int i = 0; i < m; i++)
        cout << x_local[i] << " ";
    cout << endl;

    // -------------------------------------------------------------------------
    // Поиск максимума в локальном куске (параллельные вычисления)
    // -------------------------------------------------------------------------
    // Это и есть суть параллельного алгоритма: вместо последовательного
    // прохода по всем n элементам на одном процессе, каждый процесс
    // обрабатывает только свои m элементов одновременно с остальными.
    int local_max = x_local[0];
    for (int i = 1; i < m; i++)
    {
        if (local_max < x_local[i])
        {
            local_max = x_local[i];
        }
    }

    // -------------------------------------------------------------------------
    // MPI_Reduce — сбор локальных максимумов в глобальный максимум
    // -------------------------------------------------------------------------
    // Reduce — обратная операция к Scatter/Broadcast: каждый процесс
    // вносит своё значение, они объединяются заданной операцией,
    // и результат оказывается на корневом процессе.
    //
    // Параметры MPI_Reduce:
    //   &local_max     — указатель на локальное значение (вход, у каждого своё)
    //   &global_max    — указатель на результат (выход, значим только на root)
    //   1              — количество элементов (сводим по одному числу)
    //   MPI_INT        — тип данных
    //   MPI_MAX        — операция редукции: берём максимум из всех local_max.
    //                    Другие варианты: MPI_SUM, MPI_MIN, MPI_PROD,
    //                    MPI_LAND (логическое И), MPI_LOR (логическое ИЛИ) и др.
    //   1              — rank процесса, который получит результат (root = 1).
    //                    Обратите внимание: root здесь процесс 1, а не 0!
    //   MPI_COMM_WORLD — коммуникатор
    MPI_Reduce(&local_max, &global_max, 1, MPI_INT, MPI_MAX, 1, MPI_COMM_WORLD);

    // -------------------------------------------------------------------------
    // Вывод глобального максимума (только на процессе 1 — он root у Reduce)
    // -------------------------------------------------------------------------
    // global_max имеет корректное значение ТОЛЬКО на процессе 1.
    // На остальных процессах переменная global_max содержит мусор —
    // поэтому вывод строго в ветке rank == 1.
    if (rank == 1)
    {
        cout << "global_max: " << global_max << endl;
    }

    // -------------------------------------------------------------------------
    // Вывод исходного глобального массива (только на процессе 0)
    // -------------------------------------------------------------------------
    // x_global выделен и заполнен только на процессе 0.
    // На остальных процессах указатель равен nullptr — разыменовывать нельзя.
    if (rank == 0)
    {
        cout << "x_global: ";
        for (int i = 0; i < n; i++)
            cout << x_global[i] << " ";
        cout << endl;
        delete[] x_global; // Освобождаем динамически выделенную память
    }

    // -------------------------------------------------------------------------
    // Завершение MPI-окружения
    // -------------------------------------------------------------------------
    // Все процессы обязаны вызвать MPI_Finalize — она освобождает ресурсы MPI.
    // После этого вызова MPI-функции недоступны. Является неявным барьером.
    MPI_Finalize();
    return 0;
}
